package com.itheima.demo2;

public class SelfIncreasingAndSelfDecreasing {
    public static void main(String[] args){
        //++表示变量的值加1
        //--表示变量的值减1
        //++和--无论是放前面还是后面，单独写一行运算结果都是一样的

        int a = 10;
        System.out.println(a);
        a++;
        System.out.println(a);
        //后++先用后加
        //前++先加后用


    }
}
//后++先用后加
//前++先加后用
