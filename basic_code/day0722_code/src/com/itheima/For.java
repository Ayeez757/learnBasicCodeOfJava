package com.itheima;

public class For {
    public static void main(String[] args) {
         /*

    for(初始化语句;条件判断语句;条件控制语句){
        循环体语句
    }

     */

        for (int i = 1; i <= 10; i++) {
            System.out.println("HelloWorld" + i);

        }

        for (int j = 1; j <= 5; j++) {
            System.out.println(j);
        }
    }
    //先用后加

    //变量的范围属于当前的大括号里，在括号外面变量无效(作用域)

}
